def get_sla_key_for_soc(soc: str) -> SlaKey | None:
    idx = SOC_TO_SLA_KEY_INDEX.get(soc.upper())
    if idx is None:
        return None
    return ALL_SLA_KEYS[idx - 1]


def find_sla_key_by_modulus(modulus: bytes) -> SlaKey | None:
    for k in ALL_SLA_KEYS:
        if k.modulus == modulus:
            return k
    return None


